public class Turtle {
    public int ageInMonth;
    public int topSpeed;

    public Turtle() {}

    public Turtle(int ageInMonth, int topSpeed) {
        this.ageInMonth = ageInMonth;
        this.topSpeed = topSpeed;
    }

    public int getAgeInMonth() {
        return ageInMonth;
    }

    public int getTopSpeed() {
        return topSpeed;
    }
}
